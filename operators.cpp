#include <iostream>
using namespace std;

int main()
{
    int a = 5, b = 20;

    cout << "Addition (a+b) : " << (a + b) << endl;
    cout << "Substraction (a-b) : " << (a - b) << endl;
    cout << "Multipliaction (a*b) : " << (a * b) << endl;
    cout << "Division (a/b) : " << (a / b) << endl;
    cout << "moduls (a%b) : " << (a % b) << endl;

    cout << "Is a equal to b ? (a == b): " << (a == b) << endl;
    cout << "Is a not equal to b ? (a != b): " << (a != b) << endl;
    cout << "Is a less then b ? (a < b): " << (a < b) << endl;
    cout << "Is a greater then or equal to 5? ? (a >= 5): " << (a >= 5) << endl;

    return 0;
};
